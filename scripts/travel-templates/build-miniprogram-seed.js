const fs = require("fs");
const path = require("path");
const { root, readTemplates, validateTemplate } = require("./lib");

const templates = readTemplates();
templates.forEach((template) => {
  const errors = validateTemplate(template);
  if (errors.length) throw new Error(`${template.id}: ${errors.join("; ")}`);
});

const outputPath = path.join(root, "apps/wechat-miniprogram/data/generatedTravelTemplates.js");
const source = `// Generated by scripts/travel-templates/build-miniprogram-seed.js.\nmodule.exports = ${JSON.stringify({ templates }, null, 2)};\n`;
fs.writeFileSync(outputPath, source);
console.log(`Wrote ${path.relative(root, outputPath)}`);
